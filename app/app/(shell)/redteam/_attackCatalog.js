/* Aggregate counts for the overview KPI tiles. */
export function attackStats() {
  const byCategory = {};
  const bySeverity = {};
  const byLocale = {};
  for (const a of ATTACKS) {
    byCategory[a.category] = (byCategory[a.category] || 0) + 1;
    bySeverity[a.severity] = (bySeverity[a.severity] || 0) + 1;
    byLocale[a.locale]     = (byLocale[a.locale]     || 0) + 1;
  }
  return { total: ATTACKS.length, byCategory, bySeverity, byLocale };
}
